a = 20
b = 10

# Equal to
print("a == b:", a == b)  # False, because 10 is not equal to 5

# Not equal to
print("a != b:", a != b)  # True, because 10 is not equal to 5

# Greater than
print("a > b:", a > b)    # True, because 10 is greater than 5

# Less than
print("a < b:", a < b)    # False, because 10 is not less than 5

# Greater than or equal to
print("a >= b:", a >= b)  # True, because 10 is greater than 5

# Less than or equal to
print("a <= b:", a <= b)  # False, because 10 is not less than or equal to 5


